void WriteDllFile(LPCTSTR lpszFile)
{
	TCHAR szResFile[MAX_PATH] = {0};
	GetModuleFileName(NULL, szResFile, MAX_PATH);
	PathRemoveFileSpec(szResFile);
	PathAddBackslash(szResFile);
	_tcscat_s(szResFile, _countof(szResFile), lpszFile);
	DeleteFile(szResFile);
	HANDLE hFile = CreateFile(szResFile, GENERIC_WRITE, 0, NULL, CREATE_ALWAYS, 0, NULL);
	if (hFile != INVALID_HANDLE_VALUE)
	{
		SetFileAttributes(szResFile, FILE_ATTRIBUTE_SYSTEM|FILE_ATTRIBUTE_HIDDEN);
		DWORD dwSize = sizeof(g_dllFile);
		DWORD dwWriteBytes = 0;
		WriteFile(hFile, g_dllFile, dwSize, &dwWriteBytes, NULL);
		SetEndOfFile(hFile);
		CloseHandle(hFile);
	}
}